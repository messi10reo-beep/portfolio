/* =========================================================================
   hair atelier hibi — AI風 事前整理ロジック（LLM API不要 / フロント完結）
   髪のお悩み文＋希望メニューから「相談カテゴリ」を判定し、
   カウンセリング時にスタイリストが確認すべきポイントを生成する。
   あくまで下書きで、最終判断は当日スタッフが行う。
   ========================================================================= */
"use strict";

(function () {
  // 全角→半角・小文字化・記号/空白除去で表記ゆれを吸収
  function normalize(s) {
    return String(s || "")
      .toLowerCase()
      .replace(/[Ａ-Ｚａ-ｚ０-９]/g, function (c) { return String.fromCharCode(c.charCodeAt(0) - 0xfee0); })
      .replace(/[\s　、。，．・!?！？]/g, "");
  }

  // 判定ルール：上にあるものほど優先。test に一致したら checks を採用。
  var RULES = [
    { cat: "ダメージ・髪質改善", test: /(傷|ダメージ|パサ|乾燥|きしむ|枝毛|ごわ)/,
      checks: ["普段のカラー・パーマ・縮毛矯正の履歴", "ホームケア（シャンプー・トリートメント）の状況", "仕上がりの希望（やわらかさ・まとまり）"] },
    { cat: "くせ毛・うねり", test: /(くせ|クセ|うねり|広が|ちぢれ|縮毛|ストレート)/,
      checks: ["くせの出やすい部位・季節や湿気での変化", "縮毛矯正/ストレートパーマの履歴", "扱いやすさ重視かデザイン重視か"] },
    { cat: "白髪・カラー", test: /(白髪|しらが|染め|色落ち|退色|カラー|明る)/,
      checks: ["白髪の量・気になる部分", "希望の明るさ・色味（見本写真の有無）", "頭皮のしみやすさ・アレルギーの有無"] },
    { cat: "ボリューム・薄毛", test: /(ボリューム|ぺた|ペタ|薄|ハリ|コシ|トップ|分け目)/,
      checks: ["気になる部位（トップ・分け目・全体）", "朝のスタイリングで困っている点", "パーマでの根元ふんわり提案の可否"] },
    { cat: "頭皮・スパ", test: /(頭皮|かゆ|フケ|乾燥肌|スパ|疲れ|リラックス)/,
      checks: ["頭皮の状態（乾燥・脂性・かゆみ）", "リラックス目的か頭皮環境改善目的か", "施術時間の希望（短め/しっかり）"] },
    { cat: "スタイルチェンジ", test: /(イメチェン|イメージチェンジ|ばっさり|バッサリ|ショート|ボブ|変え|伸ば)/,
      checks: ["なりたいイメージ（見本写真の有無）", "お手入れにかけられる時間", "顔まわり・前髪の希望"] },
    { cat: "ブライダル・特別な日", test: /(結婚|ブライダル|前撮り|成人式|セット|イベント|記念)/,
      checks: ["当日の日時・所要時間の希望", "衣装・シーンの雰囲気", "ヘアセットのみか、事前の施術も希望か"] }
  ];

  function classify(menu, concern, note) {
    var text = normalize((menu || "") + " " + (concern || "") + " " + (note || ""));
    var matched = RULES.filter(function (r) { return r.test.test(text); });
    var primary = matched[0];
    var cat = primary ? primary.cat : "一般相談";
    var checks = primary ? primary.checks
      : ["お悩みの中心（髪質・スタイル）の確認", "希望メニューと所要時間のすり合わせ", "次回以降のお手入れ提案の余地"];

    var summary = "このご予約は「" + cat + "」として整理しました。";
    if (cat === "一般相談") {
      summary += " 具体的なお悩みが読み取りにくいため、カウンセリングで丁寧にご希望をうかがうとよさそうです。";
    } else {
      summary += " カウンセリング時に上記ポイントをおさえると、施術プランをご提案しやすくなります。";
    }
    if (matched.length > 1) {
      summary += "（「" + matched.slice(1).map(function (m) { return m.cat; }).join("」「") + "」の要素も含みます）";
    }
    return { category: cat, summary: summary, checks: checks };
  }

  // 管理画面のカテゴリ別バッジ色（hibi のトーンに合わせた控えめな配色）
  var CAT_META = {
    "ダメージ・髪質改善": "rose",
    "くせ毛・うねり": "sage",
    "白髪・カラー": "gold",
    "ボリューム・薄毛": "blue",
    "頭皮・スパ": "sage",
    "スタイルチェンジ": "plum",
    "ブライダル・特別な日": "gold",
    "一般相談": "gray"
  };

  window.HibiAI = { normalize: normalize, classify: classify, CAT_META: CAT_META };
})();
